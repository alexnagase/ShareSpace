public class UserInformation {

    public String name;
    public String phoneNumber;
    public String email;
    public String contactPreference;


    public UserInformation(String name, String phoneNumber, String email, String contactPreference){
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.contactPreference = contactPreference;

    }

}
